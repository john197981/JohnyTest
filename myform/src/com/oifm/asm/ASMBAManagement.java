package com.oifm.asm;


public class ASMBAManagement {
	
	private int id;
	private String heading;
	private String subheading;
	private String name;
	private String designation;
	private String division;
	private String profile;
	private String divisionurl;
	private String photograph;

	/**
	 * @return Returns the designation.
	 */
	public String getDesignation() {
		return designation;
	}
	/**
	 * @param designation The designation to set.
	 */
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	/**
	 * @return Returns the division.
	 */
	public String getDivision() {
		return division;
	}
	/**
	 * @param division The division to set.
	 */
	public void setDivision(String division) {
		this.division = division;
	}
	/**
	 * @return Returns the divisionurl.
	 */
	public String getDivisionurl() {
		return divisionurl;
	}
	/**
	 * @param divisionurl The divisionurl to set.
	 */
	public void setDivisionurl(String divisionurl) {
		this.divisionurl = divisionurl;
	}
	/**
	 * @return Returns the heading.
	 */
	public String getHeading() {
		return heading;
	}
	/**
	 * @param heading The heading to set.
	 */
	public void setHeading(String heading) {
		this.heading = heading;
	}
	/**
	 * @return Returns the id.
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id The id to set.
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return Returns the photograph.
	 */
	public String getPhotograph() {
		return photograph;
	}
	/**
	 * @param photograph The photograph to set.
	 */
	public void setPhotograph(String photograph) {
		this.photograph = photograph;
	}
	/**
	 * @return Returns the profile.
	 */
	public String getProfile() {
		return profile;
	}
	/**
	 * @param profile The profile to set.
	 */
	public void setProfile(String profile) {
		this.profile = profile;
	}
	/**
	 * @return Returns the subheading.
	 */
	public String getSubheading() {
		return subheading;
	}
	/**
	 * @param subheading The subheading to set.
	 */
	public void setSubheading(String subheading) {
		this.subheading = subheading;
	}
}
