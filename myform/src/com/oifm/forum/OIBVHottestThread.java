package com.oifm.forum;

public class OIBVHottestThread 
{
    private String title;
    private String threadId;
    private String latestFlag;
    private String topicId;
    private String topicName;
    private String postedOn;
    private String numberOfPosts;
    
    /**
     * @return Returns the postedOn.
     */
    public String getPostedOn() {
        return postedOn;
    }
    /**
     * @param postedOn The postedOn to set.
     */
    public void setPostedOn(String postedOn) {
        this.postedOn = postedOn;
    }
    /**
     * @return Returns the topicId.
     */
    public String getTopicId() {
        return topicId;
    }
    /**
     * @param topicId The topicId to set.
     */
    public void setTopicId(String topicId) {
        this.topicId = topicId;
    }
    /**
     * @return Returns the topicName.
     */
    public String getTopicName() {
        return topicName;
    }
    /**
     * @param topicName The topicName to set.
     */
    public void setTopicName(String topicName) {
        this.topicName = topicName;
    }
    /**
     * @return Returns the latestFlag.
     */
    public String getLatestFlag() {
        return latestFlag;
    }
    /**
     * @param latestFlag The latestFlag to set.
     */
    public void setLatestFlag(String latestFlag) {
        this.latestFlag = latestFlag;
    }
    /**
     * @return Returns the threadId.
     */
    public String getThreadId() {
        return threadId;
    }
    /**
     * @param threadId The threadId to set.
     */
    public void setThreadId(String threadId) {
        this.threadId = threadId;
    }
    /**
     * @return Returns the title.
     */
    public String getTitle() {
        return title;
    }
    /**
     * @param title The title to set.
     */
    public void setTitle(String title) {
        this.title = title;
    }
    /**
     * @return Returns the numberOfPosts.
     */
    public String getNumberOfPosts() {
        return numberOfPosts;
    }
    /**
     * @param numberOfPosts The numberOfPosts to set.
     */
    public void setNumberOfPosts(String numberOfPosts) {
        this.numberOfPosts = numberOfPosts;
    }
}
